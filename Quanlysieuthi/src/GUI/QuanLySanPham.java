    private void load() {
        ArrayList<Product> LSP = ProductDAO.layDanhSachSanPham();
        dtm.setRowCount(0);
        for (Product sp : LSP) {
            Vector<Object> v = new Vector<Object>();
            v.add(sp.getProductID());
            v.add(sp.getCategoryName());
            v.add(sp.getProductName());
            v.add(sp.getProductDescription());
            v.add(sp.getProductQuantity());
            v.add(sp.getProductPrice());
            dtm.addRow(v);
        }
    }

    private void imgProduct(String link, JLabel lb) {
        ImageIcon icon = new ImageIcon(link);
        icon.setImage(icon.getImage().getScaledInstance(lb.getWidth(), lb.getHeight(), Image.SCALE_DEFAULT));
        lb.setIcon(icon);
    }

