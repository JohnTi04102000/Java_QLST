    private void btnTimActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTimActionPerformed
        // TODO add your handling code here:
        ArrayList<Product> LSP = ProductDAO.timKiemSanPham(txtTimKiem.getText());
        dtm.setRowCount(0);
        for (Product sp : LSP) {
            Vector<Object> v = new Vector<Object>();
            v.add(sp.getProductID());
            v.add(sp.getCategoryName());
            v.add(sp.getProductName());
            v.add(sp.getProductDescription());
            v.add(sp.getProductQuantity());
            v.add(sp.getProductPrice());
            dtm.addRow(v);
        }

    }//GEN-LAST:event_btnTimActionPerformed

    private void btnThemSPActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemSPActionPerformed
        // TODO add your handling code here:
        int CategotyID = CategoryDAO.GetCategoryIdByName(txtLoaiSP.getText());
        String tenSP = txtTenSP.getText();
        String moTa = txtMotaSP.getText();
        int soLuong = Integer.parseInt(txtSLSP.getText());
        double gia = Double.parseDouble(txtGiaSP.getText());
        Product sp = new Product();
        sp.setProductName(tenSP);
        sp.setProductDescription(moTa);
        sp.setProductQuantity(soLuong);
        sp.setProductPrice(gia);
        sp.setCategoryID(CategotyID);
        ProductDAO.themSanPham(sp);
        load();
        JOptionPane.showConfirmDialog(this, "Thêm thành công", "Thông báo",
                JOptionPane.INFORMATION_MESSAGE);

    }//GEN-LAST:event_btnThemSPActionPerformed

